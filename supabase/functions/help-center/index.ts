serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages, addressPreference = "plural" } = await req.json();
    const OPENAI_API_KEY = Deno.env.get("OPENAI_API_KEY");
    
    if (!OPENAI_API_KEY) {
      throw new Error("OPENAI_API_KEY is not configured");
    }

    // Build address instruction based on preference
    let addressInstruction = "";
    switch (addressPreference) {
      case "male":
        addressInstruction = "פנה למשתמש בלשון זכר יחיד. לדוגמה: 'היכנס', 'לחץ', 'בחר', 'אתה יכול'.";
        break;
      case "female":
        addressInstruction = "פני למשתמשת בלשון נקבה יחידה. לדוגמה: 'היכנסי', 'לחצי', 'בחרי', 'את יכולה'.";
        break;
      case "plural":
      default:
        addressInstruction = "השתמש בלשון רבים (נכנסים, לוחצים) או בצורה המכלילה עם קו נטוי (היכנס/י, לחץ/י).";
        break;
    }

    const dynamicPrompt = SYSTEM_PROMPT.replace(
      "חשוב מאוד - שפה מכלילה מגדרית:",
      `חשוב מאוד - שפה מכלילה מגדרית:\n${addressInstruction}\n\nהנחיה כללית:`
    );

    const response = await fetch("https://api.openai.com/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${OPENAI_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "gpt-4o-mini",
        messages: [
          { role: "system", content: dynamicPrompt },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "יותר מדי בקשות, נסה שוב בעוד כמה שניות" }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "אנא צור קשר עם התמיכה" }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const text = await response.text();
      console.error("AI gateway error:", response.status, text);
      return new Response(JSON.stringify({ error: "שגיאה בשרת, נסה שוב" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("help-center error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
